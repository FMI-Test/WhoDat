-- Fetch Group, Nested-Group, Manager, & Member data
SELECT dig.GROUP_ID
	, dig.LDAP_HOST
	, dig.LDAP_OU
	, dig.GROUP_NAME
    , REGEXP_SUBSTR(digm.MEMBER, 'cn=([^,]+)', 1, 1, NULL, 1) AS NESTED_GROUP
	, dig.PRIMARY_MANAGER_SSO AS GM_SSO
	--, TRIM (',' FROM iu.FIRST_NAME || ' ' || iu.MIDDLE_NAME  || ', ' || iu.LAST_NAME) AS GM_NAME
	, iu.EMAIL_DISPLAY_NAME AS GM_EMAIL_DISP
    --, iu.EMAIL_ADDRESS AS GM_EMAIL_ADD
    , iu.PERSON_STATUS AS GM_STATUS
    , iu.PERSON_TYPE AS GM_TYPE
    , iu.JOB_TITLE AS GM_TITLE
    , iu.JOB_FUNCTION AS GM_FUNC
    --, iu.SSO_EXPIRATION_DATE AS GM_EXP_DATE
    --, iu.LAST_UPDATE_DATE AS GM_UPD_DATE
    --, iu.EFFECTIVE_DATE AS GM_EFF_DATE
    , iu.INDUSTRY_FOCUS_NAME AS GM_BU
    , iu.BUSINESS_SEGMENT AS GM_BU_SEG
    --, iu.COMPANYNAME AS GM_COM
    , iu.DEPARTMENT_NAME AS GM_DEP
	, ium.PERSON_NUM_SSO AS GU_SSO
	--, TRIM (',' FROM ium.FIRST_NAME || ' ' || ium.MIDDLE_NAME  || ', ' || ium.LAST_NAME) AS GU_NAME
	, ium.EMAIL_DISPLAY_NAME AS GU_EMAIL_DISP
    --, ium.EMAIL_ADDRESS AS GU_EMAIL_ADD
    , ium.PERSON_STATUS AS GU_STATUS
    , ium.PERSON_TYPE AS GU_TYPE
    , ium.JOB_TITLE	AS GU_TITLE
    , ium.JOB_FUNCTION AS GU_FUNC
    --, ium.SSO_EXPIRATION_DATE AS GU_EXP_DATE
    --, ium.LAST_UPDATE_DATE AS GU_UPD_DATE
    --, ium.EFFECTIVE_DATE AS GU_EFF_DATE
    , ium.INDUSTRY_FOCUS_NAME AS GU_BU
    , ium.BUSINESS_SEGMENT AS GU_BU_SEG
    , ium.DEPARTMENT_NAME AS GU_DEP
FROM IDM.DW_IDM_GROUP dig 
JOIN IDM.IDM_USER iu ON dig.PRIMARY_MANAGER_SSO = iu.PERSON_NUM_SSO	
JOIN IDM.DW_IDM_GROUP_MEMBER digm ON dig.GROUP_ID = digm.GROUP_ID
JOIN IDM.IDM_USER ium ON digm.MEMBER = ium.PERSON_NUM_SSO	
	WHERE dig.GROUP_NAME LIKE '@GE Cloud%' 
		-- Filter for Commercial & GovCloud DLs which ends to 12 digits AWS Account ID
		OR ( dig.GROUP_NAME LIKE '@GE AWS_%' AND REGEXP_LIKE(dig.GROUP_NAME, '[0-9]{12}$') )
		OR ( dig.GROUP_NAME LIKE '@GE GOVAWS_%' AND REGEXP_LIKE(dig.GROUP_NAME, '[0-9]{12}$') )
	ORDER BY dig.GROUP_NAME ASC, dig.GROUP_ID ASC

